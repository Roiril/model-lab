# -*- coding: utf-8 -*-
"""アルゴ (algo) カードの寸法・配色（単位: m）。

ボードゲーム「アルゴ」の数字カード（0〜11 の白カード／黒カード）を
テクスチャ付き 3D モデルとして再構築するためのパラメータ。
実物写真（白=白地に黒数字 / 黒=黒地に白数字）から配色・比率を採寸。
"""

# --- カード寸法 -----------------------------------------------------------
CARD_W = 0.042      # 42mm 幅（公式寸法: ボドゲインスト部 42×66mm）
CARD_H = 0.066      # 66mm 高さ（縦長カード）
CARD_R = 0.0035     # 角丸半径 3.5mm
CARD_T = 0.0020     # 厚み 2.0mm（Unity 表示用。実カードより厚い）

VALUES = list(range(0, 12))   # 0〜11

# --- 配色（sRGB 0-255）---------------------------------------------------
# 実物はマット。純白／純黒を少し寄せて自然な紙色・インク色に。
WHITE_BODY = (247, 246, 242)   # 白カード地
WHITE_NUM  = (24, 22, 20)      # 白カードの数字（黒）
BLACK_BODY = (26, 25, 24)      # 黒カード地
BLACK_NUM  = (247, 246, 242)   # 黒カードの数字（白）

# 側面色は表地よりわずかに沈ませる（厚み方向の陰影を出す）
def _dim(rgb, k=0.88):
    return tuple(int(c * k) for c in rgb)

WHITE_SIDE = _dim(WHITE_BODY)
BLACK_SIDE = _dim(BLACK_BODY)
