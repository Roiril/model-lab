"""quarter_sphere の寸法・定数（単位: m。コメントに mm 併記）

8分割版: 直径400mmの中空球を 縦2面(x=0,y=0) + 赤道面(z=0) で計8個に分割。
出力は1パーツ（上半球の1/4=オクタント）。上半球用に4個、下半球用に4個
（上下対称なので同じパーツを天地反転して使う）印刷して組む。
"""
import math

# --- 球本体 ---
R_OUT = 0.200            # 外半径 200mm（直径 400mm）
WALL  = 0.003            # 肉厚 3mm
R_IN  = R_OUT - WALL     # 内半径 197mm

# 球面メッシュ解像度（高いほど滑らか・重い）
SPHERE_SEG  = 128
SPHERE_RING = 96

# --- 接合フランジ（合わせ面の内側のリブ。ダボ穴の土台） ---
FLANGE_T     = 0.008     # フランジ厚 8mm（合わせ面から内側へ）
FLANGE_DEPTH = 0.018     # フランジ深さ 18mm（球内面から半径内向きへ）

# --- 位置決め: 突き出しピンは廃止。両面とも「穴」のみ。
#     別体のダボ（径5mm丸棒 / フィラメント / 印刷ダボ）を差して位置決め。
DOWEL_D    = 0.005       # ダボ径 5mm
DOWEL_CLR  = 0.0003      # クリアランス 0.3mm / 側
HOLE_D     = DOWEL_D + 2 * DOWEL_CLR   # 穴径 5.6mm
HOLE_DEPTH = 0.007       # 各穴の深さ 7mm（フランジ厚 8mm に 1mm 残す）
# 推奨ダボ長 = 2*HOLE_DEPTH - 1mm ≒ 13mm

# 縦シーム(x=0 / y=0 面)のダボ穴を置く高さ Z（上半球内）。
# 両面・全パーツ同一なので 90°回転コピー同士が必ず一致。
VHOLE_Z = [0.04, 0.10, 0.15]

# 赤道シーム(z=0 面)のダボ穴を置く方位角[rad]。軸はZ（垂直）。
EHOLE_AZ = [math.radians(30.0), math.radians(60.0)]
